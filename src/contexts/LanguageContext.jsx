export const LanguageProvider = ({ children }) => {
  // localStorage'dan dil yÃ¼kle - ANINDA (F5 sonrasÄ± hemen uygula)
  const getInitialLanguage = () => {
    if (typeof window === 'undefined') return 'tr'
    const saved = localStorage.getItem('language')
    return saved || 'tr'
  }

  const [language, setLanguage] = useState(getInitialLanguage)

  // Dil deÄŸiÅŸikliklerini localStorage'a kaydet - ANINDA
  useEffect(() => {
    localStorage.setItem('language', language)
  }, [language])

  // MongoDB'den gelen language deÄŸiÅŸikliklerini dinle
  useEffect(() => {
    const handleLanguageChange = (event) => {
      const newLanguage = event.detail
      setLanguage(newLanguage)
      localStorage.setItem('language', newLanguage) // localStorage'a da kaydet
    }
    
    window.addEventListener('languageChanged', handleLanguageChange)
    return () => window.removeEventListener('languageChanged', handleLanguageChange)
  }, [])

  const changeLanguage = (lang) => {
    setLanguage(lang)
    localStorage.setItem('language', lang) // AnÄ±nda localStorage'a kaydet
  }

  const t = (key, params = {}) => {
    const dict = translations[language] || translations.tr
    let translation = dict[key] || key
    // Replace params like {attempts} with actual values
    Object.keys(params).forEach(paramKey => {
      translation = translation.replace(`{${paramKey}}`, params[paramKey])
    })
    return translation
  }

  return (
    <LanguageContext.Provider value={{ language, changeLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

const useLanguage = () => {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}

export { useLanguage }


